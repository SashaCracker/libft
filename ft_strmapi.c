#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*result;
	unsigned int	i;

	i = 0;
	if ((s == 0) || (f == 0))
		return (NULL);
	result = (char *) malloc(sizeof (char) * (ft_strlen(s) + 1));
	if (result == 0)
		return (NULL);
	while (s[i] != '\0')
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
