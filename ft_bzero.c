#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*c;

	i = 0;
	c = (char *) s;
	if (n != 0)
	{
		while (i < n)
		{
			c[i] = '\0';
			i++;
		}
	}
}
